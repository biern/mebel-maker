export function resolveUserLocale(languages: readonly string[] = navigator.languages): SupportedLocale {
  for (const language of languages) {
    try {
      const locale = new Intl.Locale(language);
      const languageCode = locale.language.toLowerCase();
      if (supportedLocales.includes(languageCode as SupportedLocale)) return languageCode as SupportedLocale;
    } catch {
      const languageCode = language.split("-")[0]?.toLowerCase();
      if (supportedLocales.includes(languageCode as SupportedLocale)) return languageCode as SupportedLocale;
    }
  }
  return fallbackLocale;
}

export function initI18n(): void {
  const initialLocale = resolveUserLocale();
  init({
    fallbackLocale,
    initialLocale,
    handleMissingMessage: ({ id }) => id
  });
  document.documentElement.lang = initialLocale;
}

